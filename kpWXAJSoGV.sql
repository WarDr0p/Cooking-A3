CREATE TABLE `Fournisseur`(
`id_fournisseur` int primary key AUTO_INCREMENT NOT NULL, 
`nom_fournisseur` varchar(20), 
`Telephone_Fournisseur` char(10)
);

CREATE TABLE `Administrateur` (
	`id_admin` int primary key AUTO_INCREMENT NOT NULL, 
	`nom_admin` varchar(20) NULL,
    `mdp` varchar(256) NULL
);

CREATE TABLE `Produits`(
	`Numero_Produit` int primary key AUTO_INCREMENT NOT NULL, 
    `nom_produit` varchar(30) NULL, 
    `Categorie_Produit` varchar(20) NULL, 
    `quantite_stock` int default 0 NULL, 
    `min_stock` int default 0 NULL, 
    `max_stock` int default 0 NULL, 
    `derniere_util` datetime NULL default NOW(), 
    `prix_produit` int NULL, 
    `unite` varchar(5) NULL, 
    `id_fournisseur` int NULL,
