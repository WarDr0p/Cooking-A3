CREATE TABLE `Client` (
	`id_client` INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
	`nom_client` varchar(28) NOT NULL, 
    `prenom_client` varchar(28) NOT NULL, 
    `telephone_client` char(10) NULL, 
    `solde_client` int default 0 NOT NULL, 
    `mdp` varchar(256) NULL
    );
    
CREATE TABLE `Commande_Client` (
	`numero_commande` INT PRIMARY KEY AUTO_INCREMENT NOT NULL, 
	`date_commande` DATETIME default NOW() NOT NULL, 
	`montant` int NOT NULL, 
	`id_client` int NULL,
	foreign key(`id_client`) references `Client`(`id_client`) ON DELETE CASCADE
);

CREATE TABLE `Cdr` (
	`id_cdr` INT PRIMARY KEY auto_increment NOT NULL,
    `id_client` int null,
    foreign key(`id_client`) references Client(`id_client`) ON DELETE CASCADE
);

CREATE TABLE `Commande_Fournisseur` (
`num_com_fou` int primary key AUTO_INCREMENT NOT NULL, 
`date_com_f`DATETIME default NOW() NOT NULL, 
`montant_com_f` int NOT NULL, 
`administrateur_id_admin` int NOT NULL, 
`id_fournisseur` int NULL,
foreign key (`id_fournisseur`) references `Fournisseur`(`id_fournisseur`) ON DELETE CASCADE
);

CREATE TABLE `Recette` (
`numero_recette` int primary key AUTO_INCREMENT NOT NULL, 
`nom_recette` varchar(30) NULL, 
`type_recette` varchar(25) NULL, 
`description_recette` varchar(256) NULL, 
`prix_recette` int NOT NULL, 
`photo_recette` varchar(400) NULL, 
`nombre_com_recette` int NULL default 0, 
`royalty_cdr` int NULL, 
`id_cdr` int NULL,
foreign key(`id_cdr`) references `Cdr`(`id_cdr`) ON DELETE CASCADE
);

CREATE TABLE `Remunere` (
`id_cdr` int NOT NULL, 
`Numero_Commande` int NOT NULL, 
`montant_royalties` int ,
primary key(`id_cdr`,`Numero_commande`),
foreign key(`id_cdr`) references `Cdr`(`id_cdr`),
foreign key (`Numero_Commande`) references `Commande_Client`(`Numero_Commande`) ON DELETE CASCADE
);

CREATE TABLE `Utilise` (
`numero_recette` int NOT NULL, 
`numero_produit` int NOT NULL, 
`quantite_ingredient` int NULL default 1,
Primary key(`numero_recette`,`numero_produit`),
foreign key(`numero_recette`) references `Recette`(`numero_recette`)ON DELETE CASCADE,
foreign key(`numero_produit`) references `Produits`(`numero_produit`) ON DELETE CASCADE
);

CREATE TABLE `Valide_Recette` (
`numero_recette` int NOT NULL, 
`id_admin` int NOT NULL, 
`date_valid_rec` DATETIME default NOW(),
primary key(`numero_recette`,`id_admin`),
foreign key(`numero_recette`) references `Recette`(`numero_recette`)ON DELETE CASCADE,
foreign key(`id_admin`) references `Administrateur`(`id_admin`)ON DELETE CASCADE
);

CREATE TABLE `Contient` (
`Numero_Commande` int  NOT NULL, 
`numero_recette` int  NOT NULL, 
`nombre_recettes` int default 1,
Primary key(`Numero_Commande`,`numero_recette`),
foreign key(`numero_recette`) references `Recette`(`numero_recette`)ON DELETE CASCADE,
foreign key(`Numero_Commande`) references `Commande_Client`(`numero_commande`)ON DELETE CASCADE
);

CREATE TABLE `Recharge` (
`Numero_Produit` int NOT NULL,
`num_com_fou` int NOT NULL,
`Quantite_Prod` int default 1,
Primary key(`Numero_Produit`, `num_com_fou`),
foreign key(`Numero_Produit`) references `Produits`(`Numero_Produit`)ON DELETE CASCADE,
foreign key(`num_com_fou`) references `Commande_Fournisseur`(`num_com_fou`) ON DELETE CASCADE
);

